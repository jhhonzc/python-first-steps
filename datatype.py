# Strings
print("Hello world")
print('Hello world')
print('''Hello world''')
print("""Hello world""")
print("Hello" + "world")

# Type
type("Hellow world")
print(type("Hellow world"))

# Integer
print(type(30))

# Float
print(type(30.5))

# Boolean
print(type(True))
print(type(False))

# List [] datos modificables
print(type([10, 20, 30, 45]))
print(type(["hey", 'you', "yeah"]))
print(type([10, "hey", 32, "jojojo"]))

# Tuples () datos NO modificables "inmutables"
print(type([10, 20, 30, 45]))
print(type(["hey", 'you', "yeah"]))
print(type([10, "hey", 32, "jojojo"]))

# Dictionaries
print(type({
    "name":"jhhon",
    "lastname":"zapata",
    "nickname":"jhhonzc"
}))

# None
print(type(None))
